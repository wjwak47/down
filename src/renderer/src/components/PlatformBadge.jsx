/**
 * PlatformBadge Component
 * @param {string} platform - Platform identifier (youtube, bilibili, etc.)
 * @param {string} size - Badge size: 'sm' or 'md' (default: 'sm')
 * @param {boolean} showName - Whether to show platform name (default: false)
 */
export function PlatformBadge({ platform = 'unknown', size = 'sm', showName = false }) {
    const config = PLATFORM_CONFIG[platform] || PLATFORM_CONFIG.unknown;
    
    const sizeClasses = {
        sm: {
            container: 'h-5 px-1.5 gap-1',
            icon: 'w-3 h-3',
            text: 'text-[10px]'
        },
        md: {
            container: 'h-6 px-2 gap-1.5',
            icon: 'w-4 h-4',
            text: 'text-xs'
        }
    };
    
    const sizes = sizeClasses[size] || sizeClasses.sm;
    
    return (
        <span 
            className={`inline-flex items-center rounded-full ${config.bgColor} ${config.textColor} ${sizes.container} font-medium`}
            title={config.name}
        >
            <span className={sizes.icon}>
                {config.icon}
            </span>
            {showName && (
                <span className={sizes.text}>{config.name}</span>
            )}
        </span>
    );
}

/**
 * Get platform configuration
 * @param {string} platform - Platform identifier
 * @returns {Object} Platform configuration
 */
export function getPlatformConfig(platform) {
    return PLATFORM_CONFIG[platform] || PLATFORM_CONFIG.unknown;
}

export default PlatformBadge;
